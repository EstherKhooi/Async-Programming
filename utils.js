function calculateSumOfTwoNumbers(num1, num2) {
    return num1 + num2;
}

function multiplyTwoNumbers(num1, num2) {
    return num1 * num2;
}

function divideTwoNumbers(num1, num2) {
    return num1 / num2;
}

module.exports = {
    calculateSumOfTwoNumbers,
    multiplyTwoNumbers,
    divideTwoNumbers
}